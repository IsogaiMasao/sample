boostrapとは・・・
Twitterが開発しているCSSフレームワーク。
フォームやボタン、ナビゲーション、モーダルなど汎用的なウェブサイトのパーツのスタイルが提供されている。
このような汎用パーツは一から作ると手間がかかるが、Boostrapを使えば車輪の再発明を抑えられるため、生産性を上げることができる。


CSSとは・・・
SASS（CSSを拡張子たメタ言語）の別文法（Sassy CSS）CSS風にかける文法。
CSSを拡張したプログラミング言語。


